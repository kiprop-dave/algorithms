const isValidSudoku = (nums) =>{
	const hashCol = new Map();
	let first = true;
	for(let i = 0; i < nums.length; i++){ // Checks rows and columns
		const hashRow = new Map();
		for(let j = 0; j < nums[i].length; j++){
			if(first){
				const ind = new Map();
				hashCol.set(j,ind)
			}
			if(nums[i][j] === "."){
				continue;
			}
			if(hashRow.has(nums[i][j])){
				return false;
			}
			hashRow.set(nums[i][j],nums[i][j])
			const current = hashCol.get(j);
			if(current.has(nums[i][j])){
				return false;
			}
			current.set(nums[i][j],nums[i][j]);
		}
		first = false;
	}
	let row = 0;
	while(row < 9){
		let col = 0;
		while(col < 9){
			const box = new Map();
			for(let i = row; i < row+3; i++){
				for(let j = col; j < col+3; j++){
					if(nums[i][j] === "."){
						continue;
					}
					if(box.has(nums[i][j])){
						return false;
					}
					box.set(nums[i][j],nums[i][j]);
				}
			}
			col += 3;
		}
		row += 3;
	}
	return true;
}

